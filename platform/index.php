      <h2>How and Why</h2>

      <p>Somerville and the Boston region as a whole are suffering through a housing affordability crisis. To address it, we need to build more housing.</p>

      <p>Increased housing production, especially in dense areas near transit, helps our community in many ways:</p>

      <ul class="default">
        <li>Increasing the number of homes relieves pressure on prices</li>
        <li>Inclusionary zoning creates subsidized housing, paid for by market-rate&nbsp;housing.</li>
        <li>Shorter commutes reduce pollution and fight climate change (and reduce traffic for drivers as well!)</li>
        <li>Greater density counteracts segregation and social isolation</li>
        <li>Construction jobs help the city’s economy</li>
      </ul>

      <h2>Affordable Housing and Inclusionary Zoning</h2>

      <p>Somerville needs both subsidized and market-rate housing, and we need plenty of both. Many activists focus on the percentage of units that are designated as affordable; we prefer to maximize the number of affordable units created.</p>

      <p>In other words, we’d rather see 20 affordable homes in a large building than 5 in a small one. After all, nobody can live in a percentage of a building that isn’t built.</p>

      <p>We support policies that:</p>

      <ul class="default">
        <li>Increase the availability of all kinds of housing, including subsidized and&nbsp;market-rate</li>
        <li>Make it easier to get around on foot, on transit, or by bike.</li>
        <li>Promote mixed-use development, broadening our tax base and creating jobs in Somerville</li>
        <li>Help people of different economic and social backgrounds live in and participate in the community</li>
        <li>Reduce carbon footprint and use space and energy efficiently</li>
        <li>Help Somerville coordinate with other towns and cities to address housing and development issues regionally</li>
        <li>Make the process of planning, permitting, and building transparent and&nbsp;predictable</li>
      </ul>

