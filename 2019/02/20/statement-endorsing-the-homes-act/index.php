      <p>The following is a letter we emailed to the MA State House of Representatives on <time datetime="2019-02-20T15:07-04:00">Feb. 20, 2019</time>, regarding a proposed bill to provide protections to tenants who entered into eviction proceedings.</p>

      <blockquote>
        <p>Statement in support of the HOMES Act</p>

        <p>We write to endorse the HOMES act, bill HD.3815, presented by Rep. Michael J. Moran, as a critical and reasonable protection for tenants.</p>

        <p>As we know, having any housing court record at all can be a steep barrier to finding a new apartment. These records are both easy to find and easy to misuse. Currently, even tenants who were found in the right, tenants who were not evicted, and people who were evicted as children through no fault of their own, find it difficult or impossible to obtain rental housing. </p>

        <p>This is unjust. Those records must be sealed.</p>

        <p>It is possible to create tenant protection rules which can be counterproductive, which discourage the creation or preservation of rental housing, or which create incentives to discriminate based on race or other prejudices. Thatâ€™s why we feel this regulation is a reasonable compromise: Records of for-cause evictions will still remain available for a limited time. </p>

        <p>Like a credit report, these records of for-cause evictions will be a burden to evicted tenants, but not indefinitely. And like a credit report, they will not follow children or other co-habitants who were not involved in the financial aspects of the eviction. </p>

        <p>This solution is imperfect. It will still leave some people facing significant obstacles to the housing that is their human right. But it will reduce their number, and we do not believe that it will reduce the total amount of rental housing available to the general public. For that reason we think it is an improvement over the status quo. </p>

        <p>Sincerely,</p>

        <p>Somerville YIMBY</p>
