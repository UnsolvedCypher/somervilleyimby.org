      <p>The following is a letter we emailed to <a href="https://www.mass.gov/service-details/secretary-matthew-a-beaton">Secretary Beaton</a>, via Alexander Strysky, on <time datetime="2019-03-01T15:07-04:00">Mar. 01, 2019</time>, regarding the Union Square Redevelopment Draft Environmental Impact Report.</p>

      <blockquote>
        <p>EEA# 15889</p>

        <p>Dear Secretary Beaton:</p>

        <p>We write in support of the Union Square Redevelopment Draft Environmental Impact Report.</p>

        <p>Somerville YIMBY — Yes In My Back Yard — is a group of Somerville residents advocating for smart growth that benefits everyone. We’re a part of a nationwide movement to increase the amount of housing available to people in cities that have suffered from gentrification and expensive market rate housing.</p>

        <p>Like our friends A Better Cambridge, we support eco-friendly density and transit-centered growth to create vibrant, walkable, bikeable, livable neighborhoods with plenty of housing for all. We believe that our cities and towns are great places, and we should welcome people who want to be here by creating plenty of housing for new neighbors of all income levels.</p>

        <p>We appreciate the work that US2 has done to engage in an extensive community planning process to shape the proposed project, and would like to see the project moved forward. The project provides clear environmental benefits:</p>

        <p>The project includes a number of measures to minimize the Project’s greenhouse gas emissions. The DEIR includes a comprehensive GHG analysis that includes a number of alternatives that will be considered as the design of each building moves forward to minimize GHG emissions.</p>

        <p>The project is tightly linked with upgraded sewer and storm systems, which will reduce water contamination and improve climate resiliency.</p>

        <p>The project is critical to the completion of the Union Square MBTA station, which will enable Somerville to reduce car travel and increase sustainable mobility.</p>

        <p>Construction includes separated bike lanes and bike parking, again enhancing sustainable mobility for the community.</p>

        <p>The buildings on the site will be LEED Gold certified, with all the attendant environmental benefits that certification implies.</p>

        <p>Increased commercial and residential density near transit hubs like Union Square reduces auto trips and carbon emissions and is broadly in keeping with the sustainability goals of the city and the state.</p>

        <p>This long-awaited development is an important part of both environmental and community goals for the city of Somerville.</p>

        <p>Sincerely,</p>

        <p>Somerville YIMBY Steering Committee</p>=
