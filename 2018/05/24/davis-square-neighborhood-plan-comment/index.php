      <p>The following is a letter we emailed to Mayor Joe Curtatone, the Somerville Planning Board, and the Somerville Board of Alderman on <time datetime="2018-05-24T14:26-04:00">May 24, 2018</time>, regarding the Davis Square Neighborhood Plan.</p>

      <blockquote>
        <p>To Mayor Joe Curtatone, the Planning Board, and the Somerville Board of Aldermen:</p>

        <p>The Somerville YIMBY Steering Committee would like to thank you for this opportunity to provide feedback and comments on the Davis Square Neighborhood Plan.</p>

        <p>There are a number of things we particularly like about this plan, including its emphasis on pedestrian, public transit, and bicycle access to the square. The Dover Street pedestrian area is an excellent idea and will make it much more pleasant for people to get around within the square. The 60 second light cycle at the reconfigured intersection has the potential to improve  walking, biking, and driving. Since the full intersection rebuild will be some years away due to sewer work, we recommend that the city test pedestrian and bicycle configuration changes using paint and posts to get the best results when the final and more permanent changes are in place.</p>

        <p>In addition, we would like to propose the following changes to the plan:</p>

        <ul class="default">
          <li>Maximize height in core portions of Davis Square. We expect that six stories is feasible, with a possibility for more in the narrow area between the busway and Highland Avenue.</li>
          <li>Designate all areas within walking distance of Davis Square as Urban Residence or denser. There is no reason to have low-density Neighborhood Residence parcels so close to a commercial and transit center. Zoning these close-in areas as Urban Residence or Mid-Rise is more consistent with SomerVision’s characterization of Davis Square as an “Area to Enhance”.</li>
          <li>Ensure that the community path has a clean connection from one side of the square to the other.</li>
          <li>Permit and encourage housing uses built above the market spaces.</li>
        </ul>

        <p>Increasing the height of buildings in Davis Square is consistent with the Neighborhood Plan’s vision of buildings that play a supporting role in neighborhood design. In addition, we must be sure that both East and West Somerville share the benefits and burdens of development. It would be unfair to build everything new and tall only in the eastern half of the city.</p>

        <p>This new neighborhood plan and its healthy density will expand economic opportunities for Somerville’s citizens and create housing for new neighbors, reducing displacement of current residents. As an Area to Enhance, it’s critical that the Davis Square Neighborhood Plan takes full advantage of the area’s potential.</p>

        <p>Thank you again for your time and hard work.</p>

        <p>Sincerely,</p>

        <p>Somerville YIMBY Steering Committee</p>

        <p>Jeff Byrnes</p>

        <p>Luke Edson</p>

        <p>Hallah Elbeleidy</p>

        <p>Laura Evans</p>

        <p>Aaron Weber</p>
      </blockquote>
