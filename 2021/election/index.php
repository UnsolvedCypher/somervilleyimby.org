    <p>Here, we present some additional context on each candidate we endorsed, as well as candidates we consider good choices, even though we did not feel confident endorsing them.</p>

    <h2>Mayor</h2>

    <p>Somerville has three very good candidates for mayor this year, and we would not be disappointed to see Katjana Ballantyne, Mary Cassesso or Will Mbah in the mayor’s office this fall. We did, however, ultimately decide to endorse Katjana & Mary.</p>

    <h3>Endorse</h3>

    <h4>Katjana Ballantyne</h4>
    <p>Katjana supports our three key goals of housing production, reducing auto-dependence, and enhancing tenant protections. Our conversation with her and her comments on our survey gave us a great deal of confidence about her thoughtful and informed understanding of the intersection of commercial development, tax base, climate change, and funding for affordable housing. She also has the most experience on city council.</p>

    <h4>Mary Cassesso</h4>
    <p>Like Katjana & Will, Mary also supports our key goals and has a nuanced and thoughtful view about achieving them. She does not have experience on City Council, but we believe that her experience as an executive manager leading a large organization would make her very effective.</p>

    <h3>Also Good</h3>

    <h4>Will Mbah</h4>
    <p>Will didn’t fill out our survey, but we spoke with him about the issues. He too is aligned with Somerville YIMBY on increasing housing, moving away from auto-centric patterns, and strengthening tenant protections. He’s an important voice in the city and his emphasis on creating community benefits for large projects is excellent. He has spent less time on City Council than Katjana Ballantyne, but his record as a councilor has been excellent. We are, however, slightly concerned about a plan to <a href="https://www.wgbh.org/news/politics/2021/08/04/whats-at-stake-in-somervilles-mayoral-race">increase inclusionary zoning requirements he mentioned in a recent WGBH article</a>. We feel that raising the requirement above 20% risks becoming counterproductive by reducing the total amount of housing produced, harming people who need subsidized and market-rate housing alike.</p>

    <h2>Councilor At-large</h2>

    <p>We have chosen to endorse Willie Burnley Jr, Charlotte Kelly, and Jake Wilson.</p>

    <p>Eve Seitchik, Justin Klekota and Kristin Strezo are also good choices.</p>

    <h3>Endorse</h3>

    <h4>Willie Burnley Jr</h4>
    <p>Willie is in support of our key goals on increasing housing, moving away from auto-centric development, and enhancing tenant protections. He is more skeptical of market-rate housing than we are, but his approach to all the issues is deeply thought and nuanced. We believe that he would be an excellent City Councilor.</p>

    <h4>Charlotte Kelly</h4>
    <p>Charlotte aligns with us on creation of affordable housing, moving away from auto-centric development, and tenant protections. She is much more skeptical of market-rate housing than we are, but we feel that she’s still a great choice. Her response notes that &ldquo;any serious approach to housing and development here in Somerville must have thoughtful processes rooted in equity, justice, and systemic solutions,&rdquo; and we agree entirely. </p>

    <h4>Jake Wilson</h4>
    <p>Jake aligns with us on housing production, transit/parking issues, and tenant protections. His written responses reflect a nuanced approach to development and other issues and an acknowledgement of the tradeoffs necessary in governance. For example, he noted that to achieve our goal of increasing the amount of green space in the city, we’ll also need tall buildings to balance it.</p>

    <h3>Also Good</h3>

    <h4>Justin Klekota</h4>
    <p>Justin agrees with us about housing and tenant protections, but supports building more parking, which is counterproductive for both climate and traffic.</p>

    <h4>Eve Seitchik</h4>
    <p>Eve did not complete our survey, but we spoke with them briefly about the issues. Critically, they agree with us about the need to create affordable housing, and on climate and transit issues. Although we disagree with them about the relative value of market-rate housing production, Eve is a thoughtful and forceful advocate for climate, transit and housing justice.</p>

    <h4>Kristen Strezo</h4>
    <p>Kristin’s survey answers state that she aligns with us on housing production, transit/parking issues, and tenant protections. She does not support reallocating police funding to housing and social services. She is also the sole incumbent running for an at-large seat.</p>

    <h2>Ward 1</h2>

    <h4>Matt McLaughlin</h4>
    <p>Matt is running unopposed & did not respond to our survey, so we have not prepared an analysis of his positions.</p>

    <h2>Ward 2</h2>

    <p>We like both of these candidates and think that either would do an excellent job.</p>

    <h4>Steph Aman</h4>
    <p>Steph supports increased housing and tenant protections, and we also like his emphasis on trees and green space. We disagree with him about parking issues. His track record as a community organizer and activist is excellent and we are confident that he’d do a good job as a councilor as well. He was also a co-chair of the SomerVision 2040 committee, and his experience there would be very valuable on the City Council.</p>

    <h4>JT Scott</h4>
    <p>JT aligns with us on housing, transit/parking, and tenant protections. He is very skeptical of market-rate housing, but has an excellent track record of building community engagement to reach effective goals. His work guiding the project at 346 Somerville Ave (the former J.J. Sullivan building) to an excellent and unconventional outcome is a great example of his pragmatic approach. He was one of several candidates to push back against the oversimplification in our survey, and made several excellent points in our conversation about it.</p>

    <h2>Ward 3</h2>

    <h3>Endorse</h3>

    <h4>Ben Ewen-Campen</h4>
    <p>Ben is running unopposed, but answered our survey anyway. He aligns with all three of our core issues, and his written comments reveal a very thoughtful approach to city government.</p>

    <h2>Ward 4</h2>

    <h4>Jesse Clingan</h4>
    <p>Jesse is running unopposed & did not respond to our survey, so we have not prepared an analysis of his positions.</p>

    <h2>Ward 5</h2>

    <p>We have chosen to endorse Beatriz Gómez Mouakad, although Tessa Bridge would also be a good choice.</p>

    <h3>Endorse</h3>

    <h4>Beatriz Gómez Mouakad</h4>
    <p>Beatriz aligns with us on increasing overall housing supply, addressing transit/parking issues, and tenant protections. We feel that her experience working on affordable housing for Just-A-Start means that she knows both how important affordable housing is, and how to create it. She told us that she would prioritize affordable housing over market rate, but recognizes the need to make that housing economically viable through mixed-use and mixed-income&nbsp;development.</p>

    <h3>Also Good</h3>

    <h4>Tessa Bridge</h4>
    <p>Tessa is supportive of affordable housing and tenant protections, but not of market-rate housing. She’s also a great advocate for people with disabilities. In her written comments she noted that while she supports many of our positions in general, it is important to include an equity focus when considering any of them — and we agree with that.</p>

    <h2>Ward 6</h2>

    <h4>Lance Davis</h4>
    <p>Lance is running unopposed & did not respond to our survey, so we have not prepared an analysis of his positions.</p>

    <h2>Ward 7</h2>

    <p>Three of these candidates seem very good, but we have chosen to endorse Judy&nbsp;Pineda&nbsp;Neufeld.</p>

    <h3>Endorse</h3>

    <h4>Judy Pineda Neufeld</h4>
    <p>Judy aligns with us on increasing housing supply, addressing transit/parking issues, and tenant protections. She supports added density near GLX stations to create more affordable housing, an expansion of the Affordable Housing Overlay, and reduced mandatory parking requirements.</p>

    <h3>Also Good</h3>

    <h4>Alexander H Anderson</h4>
    <p>Alexander aligns with us on increasing housing supply, addressing transit/parking issues, and tenant protections. He states that he would focus on and prioritize affordable housing over market-rate, and notes that nuance and systems thinking are important.</p>

    <h4>Becca Miller</h4>
    <p>Becca aligns with us on housing, transit and tenant protections but is much more skeptical of market-rate housing.</p>
